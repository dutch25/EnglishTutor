      } catch (e) {
        return "";
      }
    },
    speakWord() {
      if (!this.result || !this.result.word) return;
      const utter = new window.SpeechSynthesisUtterance(this.result.word);
      utter.lang = "en-US";
      window.speechSynthesis.speak(utter);
